class BitmapFont:
    """Simple bitmap font for text rendering."""

    __slots__ = ('_ctx', '_texture', '_char_width', '_char_height',
                 '_chars_per_row', '_scale', '_tex_width', '_tex_height')

    CHAR_WIDTH = 8
    CHAR_HEIGHT = 8
    ATLAS_CHARS = 96  # ASCII 32-127

    def __init__(self, ctx: moderngl.Context, scale: int = 2):
        """
        Create bitmap font.

        @param ctx: ModernGL context.
        @param scale: Display scale factor for characters.
        """
        self._ctx = ctx
        self._scale = scale
        self._char_width = self.CHAR_WIDTH * scale
        self._char_height = self.CHAR_HEIGHT * scale
        self._chars_per_row = 16

        # Calculate atlas dimensions
        rows = (self.ATLAS_CHARS + self._chars_per_row - 1) // self._chars_per_row
        self._tex_width = self._chars_per_row * self.CHAR_WIDTH
        self._tex_height = rows * self.CHAR_HEIGHT

        # Generate font texture atlas
        atlas = self._generate_atlas()

        # Create texture (single channel for alpha)
        self._texture = ctx.texture(
            (self._tex_width, self._tex_height),
            1,  # Single channel
            atlas.tobytes()
        )
        self._texture.filter = (moderngl.NEAREST, moderngl.NEAREST)

    def _generate_atlas(self) -> NDArray[np.uint8]:
        """Generate font texture atlas."""
        rows = (self.ATLAS_CHARS + self._chars_per_row - 1) // self._chars_per_row
        atlas = np.zeros(
            (rows * self.CHAR_HEIGHT, self._chars_per_row * self.CHAR_WIDTH),
            dtype=np.uint8
        )

        for i in range(self.ATLAS_CHARS):
            char = chr(32 + i)
            char_data = FONT_DATA.get(char, FONT_DATA[' '])

            row = i // self._chars_per_row
            col = i % self._chars_per_row

            x_offset = col * self.CHAR_WIDTH
            y_offset = row * self.CHAR_HEIGHT

            for y, byte in enumerate(char_data):
                for x in range(8):
                    if byte & (0x80 >> x):
                        atlas[y_offset + y, x_offset + x] = 255

        return atlas

    def get_char_uv(self, char: str) -> Tuple[float, float, float, float]:
        """
        Get UV coordinates for a character.

        @param char: Character to look up.
        @returns: Tuple of (u0, v0, u1, v1) UV coordinates.
        """
        code = ord(char) - 32
        if code < 0 or code >= self.ATLAS_CHARS:
            code = 0  # Space for unknown

        row = code // self._chars_per_row
        col = code % self._chars_per_row

        u0 = (col * self.CHAR_WIDTH) / self._tex_width
        v0 = (row * self.CHAR_HEIGHT) / self._tex_height
        u1 = ((col + 1) * self.CHAR_WIDTH) / self._tex_width
        v1 = ((row + 1) * self.CHAR_HEIGHT) / self._tex_height

        return (u0, v0, u1, v1)

    @property
    def texture(self) -> moderngl.Texture:
        """Get the font texture."""
        return self._texture

    @property
    def char_width(self) -> int:
        """Get scaled character width."""
        return self._char_width

    @property
    def char_height(self) -> int:
        """Get scaled character height."""
        return self._char_height

    def release(self) -> None:
        """Release GPU resources."""
        self._texture.release()
